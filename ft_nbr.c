#include "ft_do_op.h"

int		ft_nbr(char *str)
{
	int i;
	int rslt;
	int sign;

	i = 0;
	rslt = 0;
	sign = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			sign = -1;
		}
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		rslt = rslt * 10 + str[i] - '0';
		i++;
	}
	return (sign * rslt);
}
